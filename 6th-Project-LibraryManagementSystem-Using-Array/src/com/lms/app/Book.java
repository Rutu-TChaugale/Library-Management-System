package com.lms.app;

public class Book {

	String bookName;
	float bookPrice;
	String bookAuthor;
	int bookQty;
	String bookType;
	String bookLang;
	
	public Book(String bookName, float bookPrice, String bookAuthor, int bookQty, String bookType, String bookLang)
	{
		this.bookName = bookName;
		this.bookPrice = bookPrice;
		this.bookAuthor = bookAuthor;
		this.bookQty = bookQty;
		this.bookType = bookType;
		this.bookLang = bookLang;
	}
	
	
}
